public class Person {
    private String name;
    private String lastName;
    private String age;
    private String exp;


    public void setName(String name) {
        this.name = name;
    }


    public void setLastName(String lastName) {
        this.lastName = lastName;
    }


    public void setAge(String age) {
        this.age = age;
    }

    public void setExp(String exp) {
        this.exp = exp;
    }
}
